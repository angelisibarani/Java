    public void refreshbox(){
            
        this.combox_asrama.removeAllItems();
        this.combox_mahas.removeAllItems();
        this.combox_kamar.removeAllItems();
        refresh();
        idx_mahas = 0;
        idx_asrm = 0;
        if(max_mahas>0){
        while(idx_mahas<max_mahas){
            a.connect("localhost", "db_asrama", "pbo", "pbo");
            if(a.getallocbynim(mahas.get(idx_mahas).getNim()) == null){
                this.combox_mahas.addItem(mahas.get(idx_mahas).getNama());
                
            }
            a.disconnect();
            
            idx_mahas++;    
        }
        }
        
         if(max_asrm > 0){
        while(idx_asrm<max_asrm){
            this.combox_asrama.addItem(asrm.get(idx_asrm).getNama_asrama());
            idx_asrm++;    
        }
         
        
        
        }
        
        
    }
        
    
     public void refresh(){
        a.connect("localhost", "db_asrama", "pbo", "pbo");
        this.mahas = a.getallmahas();
        this.max_mahas = a.counter;
        this.asrm = a.getallasrama();
        this.max_asrm = a.counter;
        a.disconnect();
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(this.combox_asrama.getSelectedItem() == null || this.combox_kamar.getSelectedItem() == null || this.combox_mahas.getSelectedItem() == null){
            jLabel4.setText("");
        }else{
        Mahasiswa m = null;
        Asrama as = null;
        a.connect("localhost", "db_asrama", "pbo", "pbo");
        m = a.getmahasbynama(this.combox_mahas.getSelectedItem().toString());
        as = a.getasramabynama(this.combox_asrama.getSelectedItem().toString());
        a.addalloc(as.getId(), this.combox_kamar.getSelectedItem().toString(), m.getNim(), m.getNama(), as.getNama_asrama());
        jLabel4.setText("Success !");
        a.disconnect();
        }
        
        refreshbox();
        
    }//GEN-LAST:event_jButton1ActionPerformed

